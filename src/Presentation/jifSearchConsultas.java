    private void setFilas() {
        try {
            String sql = "SELECT consulta.id_consulta, paciente.dni, CONCAT(paciente.apellido_pat, ' ', paciente.apellido_mat, ' ', "
                    + "paciente.nombres) AS 'Paciente', paciente.cel1, lugar_procedencia.nombre AS "
                    + "'Lugar de procedencia' FROM paciente INNER JOIN lugar_procedencia ON "
                    + "lugar_procedencia.id_procedencia = paciente.id_procedencia INNER JOIN historia_clinica ON "
                    + "paciente.dni = historia_clinica.dni_paciente INNER JOIN consulta ON historia_clinica.n_historia = "
                    + "consulta.n_historia";
            PreparedStatement ps = cnn.getCnn().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            Object datos[] = new Object[5];
            while (rs.next()) {
                for (int i = 0; i < 5; i++) {
                    datos [i] = rs.getObject(i + 1);
                }
                modelo_dni.addRow(datos);
                modelo_nombre.addRow(datos);
            }
            rs.close();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
