    private void cargar_paciente(String dni) {
        try {
            String sql = "SELECT paciente.apellido_pat, paciente.apellido_mat, paciente.nombres FROM paciente WHERE "
                    + "paciente.dni = '" + dni + "'";
            PreparedStatement ps = cnn.getCnn().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                factura.txtApellidoPat.setText(rs.getString(1));
                factura.txtApellidoMat.setText(rs.getString(2));
                factura.txtNombres.setText(rs.getString(3));
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
