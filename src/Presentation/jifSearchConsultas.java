    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Negocio.daoConsulta objConsulta = new Negocio.daoConsulta();
        switch (tbpHistorias.getSelectedIndex()) {
            case 0:
                factura.txtDni.setText(tbHistoriasDNI.getValueAt(tbHistoriasDNI.getSelectedRow(), 1).toString());
                cargar_paciente(tbHistoriasDNI.getValueAt(tbHistoriasDNI.getSelectedRow(), 1).toString());
                factura.txtDni.setEnabled(false);
                factura.txtApellidoPat.setEnabled(false);
                factura.txtApellidoMat.setEnabled(false);
                factura.txtNombres.setEnabled(false);
                factura.txtCuotas.requestFocus();
                factura.lblConsulta.setText(tbHistoriasDNI.getValueAt(tbHistoriasDNI.getSelectedRow(), 0).toString());
                factura.lblConsulta.setVisible(false);
                objConsulta.setId_consulta(Integer.parseInt(lblConsulta.getText()));
                factura.txtSaldo.setText(objConsulta.consultar_saldo(txtDni.getText()));
                factura.txtTotal.setText(objConsulta.consultar_total());
                dispose();
            case 1:
                factura.txtDni.setText(tbHistoriasNom.getValueAt(tbHistoriasNom.getSelectedRow(), 1).toString());
                cargar_paciente(tbHistoriasNom.getValueAt(tbHistoriasNom.getSelectedRow(), 1).toString());
                factura.txtDni.setEnabled(false);
                factura.txtApellidoPat.setEnabled(false);
                factura.txtApellidoMat.setEnabled(false);
                factura.txtNombres.setEnabled(false);
                factura.txtCuotas.requestFocus();
                factura.lblConsulta.setText(tbHistoriasNom.getValueAt(tbHistoriasNom.getSelectedRow(), 0).toString());
                factura.lblConsulta.setVisible(false);
                objConsulta.setId_consulta(Integer.parseInt(lblConsulta.getText()));
                factura.txtSaldo.setText(objConsulta.consultar_saldo(txtDni.getText()));
                factura.txtTotal.setText(objConsulta.consultar_total());
                dispose();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


