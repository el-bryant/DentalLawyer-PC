    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (consultas == null || consultas.isClosed()) {
            consultas = new jifSearchConsultas();
            Dimension ds = frmMain.lypMain.getSize();
            Dimension fs = consultas.getSize();
            consultas.setLocation((ds.width - fs.width) / 2, (ds.height - fs.height) / 2);
            frmMain.lypMain.add(consultas);
            consultas.show();
            try {
                consultas.setSelected(true);
                consultas.setMaximum(true);
                consultas.btnGuardar.setEnabled(true);
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnPagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagoActionPerformed
        if (detalle == null || detalle.isClosed()) {
            detalle = new jifDetallePago();
            Dimension ds = frmMain.lypMain.getSize();
            Dimension fs = detalle.getSize();
            detalle.setLocation((ds.width - fs.width) / 2, (ds.height - fs.height) / 2);
            frmMain.lypMain.add(detalle);
            detalle.show();
        }
    }//GEN-LAST:event_btnPagoActionPerformed


