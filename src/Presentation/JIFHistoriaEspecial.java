    private void cargar_paciente_modificar(String dni) {
        try {
            String sql = "SELECT paciente.apellido_pat, paciente.apellido_mat, paciente.nombres, paciente.telefono, "
                    + "paciente.cel1, paciente.cel2, paciente.correo, paciente.f_nacimiento, paciente.direccion, FROM "
                    + "paciente WHERE paciente.dni = '" + dni + "'";
            PreparedStatement ps = cnn.getCnn().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                modificar_paciente.txtApellidoPat.setText(rs.getString(1));
                modificar_paciente.txtApellidoMat.setText(rs.getString(2));
                modificar_paciente.txtNombres.setText(rs.getString(3));
                modificar_paciente.txtTelefono1.setText(rs.getString(4).substring(3, 5));
                modificar_paciente.txtTelefono2.setText(rs.getString(4).substring(5, 7));
                modificar_paciente.txtTelefono3.setText(rs.getString(4).substring(7, 9));
                modificar_paciente.txtCel11.setText(rs.getString(5).substring(0, 3));
                modificar_paciente.txtCel12.setText(rs.getString(5).substring(3, 6));
                modificar_paciente.txtCel13.setText(rs.getString(5).substring(6, 9));
                modificar_paciente.txtCel21.setText(rs.getString(6).substring(0, 3));
                modificar_paciente.txtCel22.setText(rs.getString(6).substring(3, 6));
                modificar_paciente.txtCel23.setText(rs.getString(6).substring(6, 9));
                modificar_paciente.txtCorreo.setText(rs.getString(7));
                modificar_paciente.dtchFnacimiento.setDate(Date.valueOf(rs.getString(8)));
                modificar_paciente.txtDireccion.setText(rs.getString(9));
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
